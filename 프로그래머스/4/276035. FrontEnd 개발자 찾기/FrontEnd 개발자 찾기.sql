SELECT DISTINCT d.ID, d.EMAIL, d.FIRST_NAME, d.LAST_NAME
FROM DEVELOPERS d
JOIN SKILLCODES s ON d.SKILL_CODE&s.CODE = s.CODE
WHERE CATEGORY='Front End'
ORDER BY d.ID ASC











